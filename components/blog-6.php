                <p>Red acne marks are a type of post-inflammatory erythema. It manifests as redness or pink discoloration on affected areas of the skin, developing after resolution of acne lesions. Inflammation triggers the release of inflammatory mediators that damage blood vessels and cause them to dilate. This increased blood flow results in the visible redness associated with post-inflammatory erythema.</p>
