            <h2>What is Acne and what causes Acne flare ups?</h2>
            <img src="/images/blog/wepik-export-2024022806011909X9_480x480.webp" alt=""> <br> <br>
            <p>While acne might seem like a random breakout, science says several factors play a role: Some people are simply more prone to acne than others because of genetics. There are multiple genes currently implicated in the development of acne but there is still no single "acne gene" that has been identified. While genetics play a role, they do not guarantee acne development. </p>
            <p>On a background of genetic predisposition to acne, the following are factors found to also contribute to acne flare-ups:</p>
            <h2>Internal Factors</h2>
            <ul>
                <li><strong>Hormonal fluctuations:</strong>  Androgen hormones can increase sebum production and clog pores, contributing to acne flare-ups, especially during puberty, menstruation, and pregnancy.</li>
                <li><strong>Stress:</strong>  Psychological stress can worsen acne by triggering the release of hormones like cortisol, which can increase inflammation and oil production. Separately, mental health conditions can cause abnormalities in gut permeability and subsequent systemic inflammation, indirectly contributing to acne flare ups.</li>
                <li><strong>Gut bacteria:</strong>  Imbalances potentially linked to breakouts as proposed in the brain-gut-skin axis model</li>
            </ul>
            <img src="/images/blog/skin-gut.webp" alt="">
            <h2>External Factors</h2>
            <ul>
                <li><strong>Improper skincare use:</strong>  Over-exfoliating, using irritating ingredients such as hydroquinone, glycolic acid, lactic acid, and benzoyl peroxide (BPO), starting active ingredients at high concentrations, or neglecting gentle cleansing can damage the skin barrier and worsen acne.</li>
                <li><strong>Comedogenic cosmetics:</strong>  Makeup and skincare products containing pore-clogging ingredients like squalane, can exacerbate acne. Go for non-comedogenic and oil-free formulas. *Please link to pore-clogging ingredients below.</li>
                <li><strong>Friction and pressure:</strong>  Repeated touching or use of equipment like electric spinners or brushes rubbing against the skin can cause frictional dermatitis and trigger breakouts due to inflammation.</li>
                <li><strong>Environmental factors:</strong>  Humidity, pollution, and excessive sun exposure can worsen acne by irritating the skin and increasing inflammation.</li>
                <li> <strong>Medications,</strong> like corticosteroids, iodine, oral contraceptives, high dose Vitamin B12 or cyanocobalamin can trigger acne flare-ups in some individuals.</li>
                <li><strong>Lifestyle:</strong>  Individuals who smoke often manifest a more severe form of acne. Smoking may also delay response to acne treatment.</li>
                <li><strong>Diet:</strong>  Although research is evolving, some studies suggest links between high glycemic index foods (white bread, sugary drinks), dairy products, and acne flares. However, evidence remains inconclusive and individual responses vary.</li>
            </ul>
            <br>
            <p>Because of these considerations, a holistic approach focusing on managing hormones, stress, diet, together with gentle skin care practices is crucial for preventing and managing acne flare-ups.</p>
